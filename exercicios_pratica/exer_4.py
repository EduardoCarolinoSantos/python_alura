#### Trabalhando com tipoos de exibição de números ####

pi_arredondado = 3.14159

print(f'O valor arredondado de pi é: {pi_arredondado:.2f}' '\n') # O :.2f é utilizado para arredondar o número para 2 casas decimais. O f é utilizado para indicar que o número é um float.

# Utilizando a função round() para arredondar o número para 2 casas decimais.

print('O valor arredondado de pi é:', round(pi_arredondado, 2))